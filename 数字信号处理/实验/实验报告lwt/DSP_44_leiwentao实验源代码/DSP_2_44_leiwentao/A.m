n=-10:10;%定义n的范围为-10~10
y1=[zeros(1,10),1,zeros(1,10)];%生成单位抽样序列
k=0:10;%定义k的范围为0~10
y2=[ones(1,11)];%生成单位阶跃序列
a1=[1,0.75,0.125];%第一个系统的y的各级系数，定义为a1矩阵
b1=[1,-1];%第一个系统的x的各级系数，定义为b1矩阵
g1=filter(b1,a1,y1);%求当输入为单位采样序列时系统的响应
g2=filter(b1,a1,y2);%求当输入为单位阶跃序列时系统的响应
a2=1;%第二个系统的y的各级参数，定义为a2矩阵
b2=[0,0.25,0.25,0.25,0.25];%第二个系统的y的各级参数，定义为b2矩阵，x（n）时系数为零
g3=filter(b2,a2,y1);%求解当输入为单位采样序列时系统的响应，即为单位抽样响应
g4=filter(b2,a2,y2);%求解当输入为单位阶跃序列时系统的响应，即为单位阶跃响应
subplot(3,2,1),stem(n,y1),title('单位采样序列');%画出y1图像，命名为单位采样序列
%subplot（m，n，p）指将图表分为m行n列，p是从从上到下从左到右第p个）
subplot(3,2,2),stem(k,y2),title('单位阶跃序列');%画出y2图像，命名为单位阶跃序列
subplot(3,2,3),stem(g1),title('单位抽样响应')%画出g1图像，命名为单位抽样响应
subplot(3,2,4),stem(g2),title('单位阶跃响应')%画出g2图像，命名为单位阶跃响应
subplot(3,2,5),stem(g3),title('单位抽样响应2')%画出g3图像，命名为单位抽样响应2
subplot(3,2,6),stem(g4),title('单位阶跃响应2')%画出g4图像，命名为单位阶跃响应2