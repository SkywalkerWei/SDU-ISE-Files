b=[0.0528,0.0797,0.1295,0.1295,0.797,0.0528];%求出分子的各系数
a=[1,-1.8107,2.1947,-1.8801,0.9537,-0.2336];%求出分母的各系数
subplot(3,1,1)%生成三行三列第一个图形
z=zplane(b,a)%绘出零极点分布图
title('零极点分布图')%输出标题为零极点分布图
[r,p,k] = residuez(b,a)%完成部分分式展开计算
x=-pi:pi/100:pi;%X的范围是-π到π间隔为0.01π
H=freqz(b,a,x);%离散时间系统频响特性的函数 freqz，
subplot(3,1,2),plot(x,abs(H))%调用abs函数求绝对值
title('幅度频率特性')
subplot(3,1,3),plot(x,180/pi*unwrap(angle(H)))%调用angle求角度再求频率
titlei('频率幅度特性')